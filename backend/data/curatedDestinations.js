// Helper function to normalize place names for lookup
export const normalizeDestinationName = (placeName) => {
  if (!placeName) return '';
  
  return placeName
    .toLowerCase()
    .replace(/[^a-z\s]/g, '') // Remove special characters
    .replace(/\s+/g, ' ')      // Normalize spaces
    .trim();
};

// Function to get curated image for a destination
export const getCuratedDestinationImage = (placeName) => {
  if (!placeName) return null;
  
  const normalizedName = normalizeDestinationName(placeName);
  
  // Direct match
  if (curatedDestinationImages[normalizedName]) {
    return curatedDestinationImages[normalizedName];
  }
  
  // Try with different variations and partial matches
  const nameVariations = [
    normalizedName,
    normalizedName.split(' ')[0], // First word only
    normalizedName.split(',')[0]?.trim(), // Part before comma
    normalizedName.replace(/\s+(city|state|country|province|region)$/i, ''), // Remove common suffixes
  ].filter(Boolean);
  
  for (const variation of nameVariations) {
    if (curatedDestinationImages[variation]) {
      return curatedDestinationImages[variation];
    }
  }
  
  // Partial matches for compound names (more thorough)
  for (const [key, imageUrl] of Object.entries(curatedDestinationImages)) {
    // Check if the key is contained in the place name or vice versa
    if (normalizedName.includes(key) || key.includes(normalizedName)) {
      return imageUrl;
    }
    
    // Check individual words for better matching
    const placeWords = normalizedName.split(' ');
    const keyWords = key.split(' ');
    
    // If any significant word matches (length > 3 to avoid common words)
    for (const placeWord of placeWords) {
      for (const keyWord of keyWords) {
        if (placeWord.length > 3 && keyWord.length > 3 && 
            (placeWord.includes(keyWord) || keyWord.includes(placeWord))) {
          return imageUrl;
        }
      }
    }
  }
  
  return null; // No curated image found
};
